#include "libft.h"
#include "fillit.h"

int	ft_lisempty(char **tab, int a)
{
	int b;

	b = 0;
	while (b < 4)
	{
		if (tab[a][b] == '.')
			b++;
		else
			return (0);
	}
	return (1);
}

#include "fillit.h"

char	*ft_seektet(char *str, int c)
{
	int i;
	int y;
	char *ret;

	i = 0;
	y = 0;
	ret = NULL;
	while (str[c] != '\0' && y != 4)
	{
		if (str[c] == '#')
			y++;
		ret[i] = str[c];
		i++;
		c++;
	}
	ret[i] = '\0';
	return (str);
}
